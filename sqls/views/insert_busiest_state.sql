INSERT INTO
    busiest_state_mv
SELECT
    AIRPORT.STATE,
    COUNT(*) NUMBER_OF_FLIGHTS
FROM
    AIRPORT AS AIRPORT
    INNER JOIN FLIGHT AS FLIGHT ON (AIRPORT.IATA_CODE = FLIGHT.ORIGIN_AIRPORT)
    OR (AIRPORT.IATA_CODE = FLIGHT.DESTINATION_AIRPORT)
GROUP BY
    AIRPORT.STATE
ORDER BY
    COUNT(*) DESC;